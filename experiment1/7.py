# 读取输入数据
m = int(input())
n = int(input())

# 计算每分钟新到游客份数y
y = (8 * m - 6 * n) / (m - n)

# 计算原有排队游客份数A
x = 8 * m - y * m

# 计算10口同时开需多少分钟才能清零待检票游客
z = x / (10 - y)

# 输出结果，保留1位小数
print("原有排队游客份数:{:.1f}, 每分钟新到游客份数:{:.1f}, 10口同开需{:.1f}分钟清零待检票游客.".format(x, y, z))

